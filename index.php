                    <h1>Winning tips and hints:</h1>
					<hr class="left">
					<p>So as you may have gathered for this competition it is I, WoodenPotatoes, that will be the sole judge of who is the winner. It's not going up to a community vote, it's not going to a commitee, it's coming to me. The spirit of the competition should be clear by now: The people who make the best videos at encouraging people to join the game and it's community are the winners. How well you and your submission accomplishes this is subjective, of course, but perhaps I can offer some further advice or guidance for those who want to get a step ahead.</p>
					<div class="long_div"></div>
					<p><i class="fa fa-angle-double-right" aria-hidden="true"></i> 1. Don't focus on the competition too much! This should probably go without saying, but if you title your video on YouTube "WoodenPotatoes Competition entry" and then spend the first thirty seconds talking about how much you'd love to win some legendaries, before finally making your case for why people might like Path of Fire and the game today, you're not going to look very authentic. It's going to be transparent that you don't really care about any of that stuff, but you're just in it for prizes. Not good. If I can notice a lack of sincerity, the potential people we're trying to reach will be able to, too.</p>
					<br>
					<p><i class="fa fa-angle-double-right" aria-hidden="true"></i> 2. Engage with your comments! This is, after all, a YouTube competition - not just a video competition. If you're linking people to the video, answering their questions, getting likes, views and so on, these are all massive bonuses to me. This competition isn't just in the spirit of growing the Guild Wars 2 playerbase, I want to see the Gw2-YouTube scene grow too! Those who are sincerely enjoying themselves and are engaging with their audience will be more deserving of a winning spot than others in my eyes.</p>
					<br>
					<p><i class="fa fa-angle-double-right" aria-hidden="true"></i> 3. Perhaps keep it short. As any fans of mine might know, this is something I'm awful at. But it's no secret that most people on YouTube have a very short attention span. Don't be scared to make your point and make it fast if you have to. It's not a requirement, of course, and some kind of super long-high effort explanation of how the game has changed over the years might just be the winning submission in the end. But the more super long entries I may have to sit through, the more tired of them I may become, and keeping it short will likely never work against you.</p>
					<br>
					<p><i class="fa fa-angle-double-right" aria-hidden="true"></i> 4. Be different, be creative, don't just add to the pile. Perhaps the more outlandish the idea the better. Don't be scared to collaborate, either: These prizes are positively enormous, you could easily split the winnings amongst multiple people and still come away ridiculously rich.</p>
					<br>
					<p>And that's about it! </p>
					<p>I'll leave you with one final note: While it may help, this competition isn't just about raw video making talent or creativity. It's not just about views, and certainly not clickbaiting your way to thousands of video hits, it's not necessarily about being a great player or an incredible artist. It's not about appealing to my personal sensibilities and values as the judge. While all of those thing are definitely to think about and can get you a leg up, they are not the be all and end all. They can't be traded for sincerity.</p>
					<p>And what's the harm in trying, right?</p>
					<p>A heartfelt and well articulated simple vlog from a person in front of a camera might be far better at convincing people to join in on Gw2 in 2017 than any number of flashy trailers, and in that situation it might be your simple and easy submission that wins you the grand prize of every legendary in the game. Or fifty bucks worth of gems.</p>
					<p>Good luck!</p>
